package com.assessment.web.dto;

public class AdaptiveAreaOfImprovement {

	
	String testName;
	
	String assessmentLevel;
	
	String skillsAssociated;
	
	String message;
	
	String recommendedcourse;
	
	String recommendedAssessment;

	public String getTestName() {
		return testName;
	}

	public void setTestName(String testName) {
		this.testName = testName;
	}

	public String getAssessmentLevel() {
		return assessmentLevel;
	}

	public void setAssessmentLevel(String assessmentLevel) {
		this.assessmentLevel = assessmentLevel;
	}

	public String getSkillsAssociated() {
		return skillsAssociated;
	}

	public void setSkillsAssociated(String skillsAssociated) {
		this.skillsAssociated = skillsAssociated;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getRecommendedcourse() {
		return recommendedcourse;
	}

	public void setRecommendedcourse(String recommendedcourse) {
		this.recommendedcourse = recommendedcourse;
	}

	public String getRecommendedAssessment() {
		return recommendedAssessment;
	}

	public void setRecommendedAssessment(String recommendedAssessment) {
		this.recommendedAssessment = recommendedAssessment;
	}
	
	
}
